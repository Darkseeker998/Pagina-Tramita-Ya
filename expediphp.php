        <h1 align="center">Expedicion de la liencia de conducción</h1>
        <p align="left">	
        La expedición de la licencia de conducción, es el trámite por medio del cual una persona solicita por primera vez el documento al organismo de tránsito, que le permite conducir un vehículo por el territorio nacional.<br><br>

        <b>Requisitos</b><br><br>

       -Presentar original del documento de identidad. Para nacionales mayores de edad: Cédula de ciudadanía- contraseña*.<br><br>

       -Para menores de edad, tarjeta de identidad, contraseña*. Para extranjeros mayores de edad: Cédula de extranjería o contraseña certificada (provisionalmente) o, pasaporte vigente.<br><br>
       
       -La solicitud es personal y el interesado debe estar inscrito en el Registro Único Nacional de Tránsito (RUNT).<br><br>

       -Estar a paz y salvo por multas e infracciones a las normas de tránsito.<br><br>

       -Pago de los derechos del trámite en los puntos de atención (ventanillas del banco).<br><br>

       -Certificado de aptitud en conducción (curso de conducción), incorporado en el RUNT, expedido por el centro de Enseñanza Automovilística (CEA), autorizado por el Ministerio de Transporte.<br><br>

       -Certificado de examen físico, mental y de coordinación motriz para conducir, incorporado en el RUNT, expedido por un Centro de Reconocimiento de Conductores (CRC) habilitado por el Ministerio de Transporte.<br><br>

       <b>Nota:</b> Tener 16 años cumplidos para obtener la Licencia de Conducción en servicio particular y los 18 años cumplidos para servicio público. Para toda solicitud de Licencia de Conducción (pase), la foto será tomada de manera gratuita en cualquiera de los doce (12) puntos de atención SIM. <br><br>

        
    	
    	
